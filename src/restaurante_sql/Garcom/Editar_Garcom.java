    public void Campos(){
        Connection con = Conexao.AbrirConexao();
        UsuarioDAO sql = new UsuarioDAO(con);
        List<Usuario> lista = new ArrayList<>();
        lista = sql.ConsultaCompleta(this.id);
       
        
        for (Usuario tab : lista) {
            //Object object = arr[j];
            
            JtfNome.setText(tab.getNome());
            JtfLogin.setText(tab.getLogin());
            JtfSenha.setText(tab.getSenha());
            JtfCpf.setText(tab.getCpf());
            JtfRg.setText(tab.getRg());
            JtfEmail.setText(tab.getEmail());
            JtfEnd.setText(tab.getEnd());
            JtfTelefone.setText(tab.getTel());
            JcbEscola.setSelectedItem(tab.getEscola());
        }
    }
    
    
