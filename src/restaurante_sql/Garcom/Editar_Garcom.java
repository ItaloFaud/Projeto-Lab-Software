    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(null, "Continuar com a alteração?");
        if(confirm == JOptionPane.YES_OPTION){
     
                    Connection con = Conexao.AbrirConexao();
                    UsuarioDAO sql = new UsuarioDAO(con);

                    Usuario u = new Usuario();
                    u.setId(this.id);
                    u.setNome(JtfNome.getText());
                    u.setLogin(JtfLogin.getText());
                    u.setSenha(JtfSenha.getText());
                    u.setCpf(JtfCpf.getText());
                    u.setRg(JtfRg.getText());
                    u.setEmail(JtfEmail.getText());
                    u.setEnd(JtfEnd.getText());
                    u.setEscola((String) JcbEscola.getSelectedItem());
                    u.setTel(JtfTelefone.getText());
                    u.setNivel(3);
                    
                    JOptionPane.showMessageDialog(null,sql.Alterar(u),"Restaurante",JOptionPane.INFORMATION_MESSAGE);
                   
                    new TelaInicial_Gerente().setVisible(true);
                     dispose();

                    Conexao.FecharConexao(con);
           
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

