    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // criação do documento
          Document document = new Document();
          try {
              File pdf = new File("Comissoes/"+this.u.getNome()+this.mes+"_"+this.ano+".pdf");
              pdf.createNewFile();
              PdfWriter.getInstance(document, new FileOutputStream("Comissoes/"+this.u.getNome()+this.mes+"_"+this.ano+".pdf"));
              document.open();
              
              // adicionando um parágrafo no documento
              document.add(new Paragraph("Comissão: "+this.u.getNome()));
              document.add(new Paragraph("Mês/Ano:"+this.mes+"/"+this.ano));
              
              document.add(new Paragraph("Data Inicial: "+LblData.getText()));
              document.add(new Paragraph("Data Final: "+LblHora.getText()));
              document.add(new Paragraph("Valor vendido: "+LblTotal.getText()));
              document.add(new Paragraph("Comissão: "+LblTotal1.getText()));
              JOptionPane.showMessageDialog(null,"Salvo!","Restaurante",JOptionPane.INFORMATION_MESSAGE);
              new TelaInicial_Gerente().setVisible(true);
              dispose();
            }
          catch(DocumentException de) {
              System.err.println(de.getMessage());
          }
          catch(IOException ioe) {
              System.err.println(ioe.getMessage());
          }
          document.close();
    }//GEN-LAST:event_jButton2ActionPerformed

