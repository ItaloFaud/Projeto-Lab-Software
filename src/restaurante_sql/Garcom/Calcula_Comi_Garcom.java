    public void AtualizaTotal(){
        Connection con = Conexao.AbrirConexao();
        ContaDAO sql = new ContaDAO(con);
        List<Conta> lista = new ArrayList<>();
        int id = this.u.getId();
        lista = sql.ConsultaFun(id);
        
        
        
        for (Conta tab : lista) {
            //Object object = arr[j];
           
            String[] data = tab.getData().split("/");
            
                if (Integer.parseInt(this.mes) == Integer.parseInt(data[1]) && Integer.parseInt(this.ano) == Integer.parseInt(data[2])){
                    this.total += Float.parseFloat(tab.getTotal());
                    this.dataf = tab.getData();
                    
                }
            
        }
    }
    
    public void Campos(){
        LblNome.setText(this.u.getNome());
        LblData.setText("01/"+mes+"/"+ano);
        LblHora.setText(this.dataf);
         
        NumberFormat nf = NumberFormat.getCurrencyInstance();// Iniciando formatação
             nf.setMaximumFractionDigits(2);
             nf.setMinimumFractionDigits(2);
        LblTotal.setText(nf.format(this.total));
        this.comi =  total/10;
         
        LblTotal1.setText(nf.format(this.comi));
        
    }

